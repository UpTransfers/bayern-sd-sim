export function normalizeTransferMarketKey(value: string) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function lookupTransferMarketIntel(name: string) {
  return transferMarketIntel[normalizeTransferMarketKey(name)] ?? null;
}

